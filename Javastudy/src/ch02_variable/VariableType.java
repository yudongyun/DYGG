package ch02_variable;

public class VariableType {
	// 변수의 타입 (기본 타입)
	
	// 정수 타입
	byte byteVar = 1;
	short shortVar = 2;
	int intVar = 3;
	long longVar = 3000000000l;
	long longVar2 = 4l;
	
	// 정수이면서 문자에 해당
	char charVar = 44032; // [가]를 의미함
	char ga = '가'; // 44032 와 같다.
	
	// 소수 타입
	double doubleVar = 3.14; //double 많이 사용, 용량이 더 큼
	float  floatVar = 3.14f; // f나 F를 붙여줘야함
	
	// 불리언 타입 (참/거짓, true/false)
	boolean boolVar = true;
	
	// 문자열 타입 (문자, char는 한글자 / 문자열은 0글자 이상)
	String strToday = "오늘은 3월 21일"; // 글자 수 제한은 없다고 봐도 무방
	
	
	
	
	
	
	
}
