package ch10_extens_interface.lol;

public class Champion {
	String name;
	int damage;
	int hp;
	
	// 모든 챔피언들은 스킬을 4개씩 가지고 있다.
	// q,w,e,r 
	
	public Champion() {}
	
	public Champion(String name, int damage, int hp) {
		super();
		this.name = name;
		this.damage = damage;
		this.hp = hp;
	}
	
	
	
	
	
	
	
	
}
